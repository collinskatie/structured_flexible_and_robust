var rating_page = {
    type: 'survey-likert',
    questions: [
        {
            prompt: function () {
                var goal = jsPsych.timelineVariable('goal')//.slice(5)
                return '<p><strong>Goal: </strong>'
                    + goal + '</p>'
                    + '<p><strong>Plan: </strong>' + jsPsych.timelineVariable('plan').replace(/\n/g, "<br />") +
                    '</p><p></p><br></br><p><strong>How plausible is it that a human would have generated the language of the plan?</strong></p>' //+
            },
            labels: scale,
            horizontal: false,
            required: true,
            name: 'humanness'
        },
    ],
    randomize_question_order: false,
};

var rating_task = {
    timeline: [rating_page],
    timeline_variables: plans_in_batch,
    data: {
        prompt: jsPsych.timelineVariable('goal'),
        task: 'rate_humanness',
        subj_id: jsPsych.timelineVariable('id'),
        plan: jsPsych.timelineVariable('plan'),
        batch_idx: jsPsych.timelineVariable('batch_idx'),
        goal_type: jsPsych.timelineVariable('goal_type')
    },
    sample: {
        type: 'custom',
        fn: function (t) {
            // t = set of indices from 0 to n-1, where n = # of trials in stimuli variable
            // returns a set of indices for trials

            // randomize order of goals + plans to show
            return jsPsych.randomization.shuffle(t)
        }
    },
    on_finish: function () {
        var curr_progress_bar_value = jsPsych.getProgressBarCompleted();
        var prop_increase = 1 / num_plans
        jsPsych.setProgressBar(curr_progress_bar_value + prop_increase);
    }
}

timeline.push(rating_task);

